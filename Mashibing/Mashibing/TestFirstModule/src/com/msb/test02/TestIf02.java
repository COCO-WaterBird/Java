package com.msb.test02;

public class TestIf02 {
    public static void main(String[] args) {


        //需求:判断一个数是否大于10
        //给定一个数;
        int num = 8;
        //判断;这个分支是二选一
        if (num > 10) {
            //num>10 -> true
            System.out.println("这个数是大于10的数字");
        } else {
            System.out.println("这个数是小于等于10的数字");
        }
    }
}





