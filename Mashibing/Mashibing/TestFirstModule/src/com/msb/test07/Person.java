package com.msb.test07;

public class Person {
    int age;
    String name;
    double height;
    //快捷键 command + n--》getter and setter
    //get...set..
    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
